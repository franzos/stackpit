/// Escape the five HTML-significant characters. Shared by the admin UI error
/// pages and HTML email bodies so neither carries its own copy.
pub fn escape_html(s: &str) -> String {
    s.replace('&', "&amp;")
        .replace('<', "&lt;")
        .replace('>', "&gt;")
        .replace('"', "&quot;")
        .replace('\'', "&#x27;")
}
